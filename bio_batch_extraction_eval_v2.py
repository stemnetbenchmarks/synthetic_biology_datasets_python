def compute_batch_eval_summary(batch_results: list) -> dict:
    """
    Aggregate per-batch evaluation results into summary statistics.

    Intent:
    -------
    This takes the list of per-batch result dicts from
    run_batch_extraction_eval() and computes aggregate metrics at three
    levels:
    1. Batch level: how many batches fully succeeded
    2. Row level: exact matches, best-fit matches, missed, spurious
    3. Field level: for best-fit matched rows, which fields caused errors

    Field-level error analysis is only possible for best-fit matches
    (where we have a plausible ground-truth pairing but some fields
    are wrong). Exact matches have zero field errors. Missed rows
    contribute to row-level stats but not field-level stats.

    Parameters:
    -----------
    batch_results : list[dict]
        The list of per-batch result dicts from run_batch_extraction_eval().

    Returns:
    --------
    dict
        A summary dictionary containing batch-level, row-level, and
        field-level statistics.
    """
    total_batches = len(batch_results)

    if total_batches == 0:
        return {
            "total_batches": 0,
            "total_extraction_failures": 0,
            "batches_all_exact": 0,
            "batch_accuracy_pct": 0.0,
            "total_rows_expected": 0,
            "total_exact_matches": 0,
            "total_best_fit_matches": 0,
            "total_missed_rows": 0,
            "total_spurious_rows": 0,
            "exact_match_rate_pct": 0.0,
            "any_match_rate_pct": 0.0,
            "per_field_stats": {},
            "total_field_errors": 0,
            "per_field_error_share_pct": {},
            "most_common_errors": [],
        }

    # --- Batch-level stats ---
    total_extraction_failures = 0
    batches_all_exact = 0

    # --- Row-level stats ---
    total_rows_expected = 0
    total_exact_matches = 0
    total_best_fit_matches = 0
    total_missed_rows = 0
    total_spurious_rows = 0

    # --- Field-level stats (from best-fit matches only) ---
    # We initialize these after we know the field names
    field_names = None
    per_field_correct = None
    per_field_incorrect = None

    for batch_result in batch_results:
        matching = batch_result["matching_result"]

        # Check for total extraction failure
        if batch_result["extracted_list"] is None:
            total_extraction_failures += 1

        # Row counts
        total_rows_expected += matching["expected_count"]
        n_exact = len(matching["exact_matches"])
        n_bestfit = len(matching["best_fit_matches"])
        n_missed = len(matching["missed_ground_truth_indices"])
        n_spurious = len(matching["spurious_extracted_indices"])

        total_exact_matches += n_exact
        total_best_fit_matches += n_bestfit
        total_missed_rows += n_missed
        total_spurious_rows += n_spurious

        # Check if this batch was perfect (all rows exact match, no extras)
        if n_exact == matching["expected_count"] and n_spurious == 0:
            batches_all_exact += 1

        # --- Field-level analysis from best-fit matches ---
        for match_info in matching["best_fit_matches"]:
            comparison = match_info["comparison"]
            field_results = comparison["field_results"]

            # Initialize field counters on first encounter
            if field_names is None:
                field_names = list(field_results.keys())
                per_field_correct = {fn: 0 for fn in field_names}
                per_field_incorrect = {fn: 0 for fn in field_names}

            for fn in field_names:
                if field_results.get(fn, False):
                    per_field_correct[fn] += 1
                else:
                    per_field_incorrect[fn] += 1

        # --- Also include exact matches in field stats (all correct) ---
        for match_info in matching["exact_matches"]:
            comparison = match_info["comparison"]
            field_results = comparison["field_results"]

            if field_names is None:
                field_names = list(field_results.keys())
                per_field_correct = {fn: 0 for fn in field_names}
                per_field_incorrect = {fn: 0 for fn in field_names}

            for fn in field_names:
                per_field_correct[fn] += 1

    # --- Compute derived stats ---
    if total_batches > 0:
        batch_accuracy_pct = (batches_all_exact / total_batches) * 100.0
    else:
        batch_accuracy_pct = 0.0

    if total_rows_expected > 0:
        exact_match_rate_pct = (total_exact_matches / total_rows_expected) * 100.0
        any_match_rate_pct = (
            (total_exact_matches + total_best_fit_matches) / total_rows_expected
        ) * 100.0
    else:
        exact_match_rate_pct = 0.0
        any_match_rate_pct = 0.0

    # --- Per-field summary ---
    per_field_stats = {}
    total_field_errors = 0
    per_field_error_share_pct = {}
    most_common_errors = []

    if field_names is not None:
        total_matched_rows = total_exact_matches + total_best_fit_matches

        for fn in field_names:
            correct = per_field_correct[fn]
            incorrect = per_field_incorrect[fn]
            total_field_errors += incorrect

            if total_matched_rows > 0:
                accuracy = (correct / total_matched_rows) * 100.0
            else:
                accuracy = 0.0

            per_field_stats[fn] = {
                "correct_count": correct,
                "incorrect_count": incorrect,
                "accuracy_pct": accuracy,
            }

        # Error share
        for fn in field_names:
            if total_field_errors > 0:
                share = (per_field_incorrect[fn] / total_field_errors) * 100.0
            else:
                share = 0.0
            per_field_error_share_pct[fn] = share

        # Most common errors sorted
        most_common_errors = sorted(
            [
                (fn, per_field_incorrect[fn], per_field_error_share_pct[fn])
                for fn in field_names
            ],
            key=lambda x: x[1],
            reverse=True,
        )

    summary = {
        "total_batches": total_batches,
        "total_extraction_failures": total_extraction_failures,
        "batches_all_exact": batches_all_exact,
        "batch_accuracy_pct": batch_accuracy_pct,
        "total_rows_expected": total_rows_expected,
        "total_exact_matches": total_exact_matches,
        "total_best_fit_matches": total_best_fit_matches,
        "total_missed_rows": total_missed_rows,
        "total_spurious_rows": total_spurious_rows,
        "exact_match_rate_pct": exact_match_rate_pct,
        "any_match_rate_pct": any_match_rate_pct,
        "per_field_stats": per_field_stats,
        "total_field_errors": total_field_errors,
        "per_field_error_share_pct": per_field_error_share_pct,
        "most_common_errors": most_common_errors,
    }

    return summary

def print_batch_eval_report(eval_summary: dict) -> None:
    """
    Pretty-print the batch evaluation summary report.

    Intent:
    -------
    Produces a human-readable report at three levels:
    1. Batch-level overview
    2. Row-level match statistics
    3. Field-level accuracy table (sorted worst-first)

    Parameters:
    -----------
    eval_summary : dict
        The summary dictionary from compute_batch_eval_summary().

    Returns:
    --------
    None
        Prints directly to stdout.
    """
    print("\n")
    print("=" * 70)
    print("  BATCH EXTRACTION EVALUATION REPORT")
    print("=" * 70)

    # --- Batch-level stats ---
    print(f"\n  BATCH-LEVEL STATISTICS")
    print(f"  {'─' * 50}")
    print(f"  Total batches evaluated:        {eval_summary['total_batches']}")
    print(f"  Extraction failures (None):     {eval_summary['total_extraction_failures']}")
    print(f"  Batches fully correct:          {eval_summary['batches_all_exact']}")
    print(f"  Batch accuracy:                 {eval_summary['batch_accuracy_pct']:.1f}%")

    # --- Row-level stats ---
    print(f"\n  ROW-LEVEL STATISTICS")
    print(f"  {'─' * 50}")
    print(f"  Total rows expected:            {eval_summary['total_rows_expected']}")
    print(f"  Exact matches:                  {eval_summary['total_exact_matches']}")
    print(f"  Best-fit matches:               {eval_summary['total_best_fit_matches']}")
    print(f"  Missed rows (dropped):          {eval_summary['total_missed_rows']}")
    print(f"  Spurious rows (hallucinated):   {eval_summary['total_spurious_rows']}")
    print(f"  Exact match rate:               {eval_summary['exact_match_rate_pct']:.1f}%")
    print(f"  Any match rate:                 {eval_summary['any_match_rate_pct']:.1f}%")

    # --- Field-level stats ---
    per_field_stats = eval_summary["per_field_stats"]
    error_shares = eval_summary["per_field_error_share_pct"]

    if per_field_stats:
        print(f"\n  FIELD-LEVEL ACCURACY (matched rows only)")
        print(f"  {'─' * 66}")
        print(f"  {'Field':<25} {'Correct':>8} {'Errors':>8} {'Accuracy':>10} {'Err Share':>10}")
        print(f"  {'─' * 66}")

        # Sort by accuracy ascending (worst first)
        sorted_fields = sorted(
            per_field_stats.keys(),
            key=lambda fn: per_field_stats[fn]["accuracy_pct"],
        )

        for fn in sorted_fields:
            stats = per_field_stats[fn]
            share = error_shares.get(fn, 0.0)
            print(
                f"  {fn:<25} "
                f"{stats['correct_count']:>8} "
                f"{stats['incorrect_count']:>8} "
                f"{stats['accuracy_pct']:>9.1f}% "
                f"{share:>9.1f}%"
            )

        print(f"  {'─' * 66}")
        print(f"  Total field errors: {eval_summary['total_field_errors']}")

        # Top errors
        fields_with_errors = [
            (fn, cnt, share)
            for fn, cnt, share in eval_summary["most_common_errors"]
            if cnt > 0
        ]

        if fields_with_errors:
            print(f"\n  TOP ERROR FIELDS (descending by error count):")
            for rank, (fn, cnt, share) in enumerate(fields_with_errors, start=1):
                print(f"    {rank}. {fn}: {cnt} errors ({share:.1f}% of all errors)")
        else:
            print(f"\n  NO FIELD ERRORS in matched rows — perfect extraction!")

    else:
        print(f"\n  No matched rows to compute field-level statistics.")

    print(f"\n{'=' * 70}")
    print()

