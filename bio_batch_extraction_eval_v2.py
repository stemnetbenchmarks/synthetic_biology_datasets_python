def concatenate_blurbs_into_batch_blob(blurbs: list) -> str:
    """
    Join multiple individual blurbs into one long text blob for batch extraction.

    Intent:
    -------
    In batch mode, the model receives one large block of text that contains
    descriptions of multiple records. This function joins the individual
    blurbs with clear separators so the model can distinguish between
    records, while still presenting them as a single unstructured input.

    The separator is a double newline. This is a neutral separator that
    doesn't impose structure (like numbering would) but gives the model
    whitespace cues to identify record boundaries.

    Parameters:
    -----------
    blurbs : list[str]
        A list of individual blurb strings, one per row.

    Returns:
    --------
    str
        A single string containing all blurbs separated by double newlines.

    Example:
    --------
    >>> concatenate_blurbs_into_batch_blob(["Blurb about cat.", "Blurb about dog."])
    "Blurb about cat.\\n\\nBlurb about dog."
    """
    batch_blob = "\n\n".join(blurbs)
    return batch_blob


