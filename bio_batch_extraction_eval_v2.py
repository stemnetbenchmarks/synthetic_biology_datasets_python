def extract_json_array_from_unstructured_text(
    dict_str: str,
    model_dict: dict,
) -> list:
    """
    Extract a JSON array of dicts from an unstructured text response.

    Intent:
    -------
    This is the batch variant of extract_json_as_pydict_from_unstructured_text().
    The key differences:
    - After extracting from markdown, expects json.loads() to produce a LIST
    - Validates that the result is a list of dicts
    - For each dict in the list: validates keys match model_dict, runs
      datatype validation/coercion
    - Returns list of validated dicts, or False on failure
    - Tolerant of getting fewer or more dicts than expected (returns whatever
      is valid; the matching logic handles discrepancies)

    This function CAN and SHOULD fail (return False) if the response is
    malformed, so the retry loop can try again.

    Parameters:
    -----------
    dict_str : str
        The raw string response from the model, expected to contain a
        markdown-formatted JSON array.
    model_dict : dict
        The target schema dictionary. Used to validate that each extracted
        dict has the correct keys.

    Returns:
    --------
    list[dict]
        A list of validated and type-coerced dictionaries on success.
    False
        On any validation failure (triggers retry upstream).
    """
    print(f"\n\n Starting extract_json_array_from_unstructured_text")

    # ========================
    # Extract from markdown
    # ========================
    try:
        pattern = r'```json\n([\s\S]*?)\n```'
        match = re.search(pattern, dict_str)
        dict_str = match.group(1) if match else ''

    except Exception as e:
        print(f"\nTRY AGAIN: extraction from markdown failed: {e}")
        print(f"Failed dict_str -> {dict_str}")
        return False

    print(f"\n Extracted from markdown -> {dict_str[:200]}...")

    # ========================
    # Safety cleaning
    # ========================
    try:
        dict_str = dict_str.replace("True", "true")
        dict_str = dict_str.replace("False", "false")
        dict_str = dict_str.replace("None", "null")

        # Remove trailing delimiter comma before closing bracket/brace
        dict_str = dict_str.replace('",\n}', '"\n}')
        dict_str = dict_str.replace(',\n]', '\n]')
        dict_str = dict_str.replace(', \n]', '\n]')

    except Exception as e:
        print(f"\nTRY AGAIN: safety cleaning failed: {e}")
        print(f"Failed dict_str -> {dict_str}")
        return False

    # ========================
    # JSON parse
    # ========================
    try:
        parsed_result = json.loads(dict_str)

    except Exception as e:
        print(f"\nTRY AGAIN: json.loads() failed: {e}")
        print(f"Failed dict_str -> {dict_str}")
        return False

    # ========================
    # Validate it is a list
    # ========================
    if not isinstance(parsed_result, list):
        print(f"\nTRY AGAIN: Expected a JSON array (list), got {type(parsed_result).__name__}")
        return False

    if len(parsed_result) == 0:
        print(f"\nTRY AGAIN: JSON array is empty (0 records extracted)")
        return False

    print(f"\n Parsed JSON array with {len(parsed_result)} items")

    # ========================
    # Validate each dict in the list
    # ========================
    validated_list = []

    for item_index, item in enumerate(parsed_result):

        # Check that each item is a dict
        if not isinstance(item, dict):
            print(
                f"\nTRY AGAIN: Item at index {item_index} is not a dict, "
                f"got {type(item).__name__}"
            )
            return False

        # Check keys match
        try:
            keys_match = item.keys() == model_dict.keys()
            if not keys_match:
                print(
                    f"\nTRY AGAIN: Item at index {item_index} has wrong keys."
                    f"\n  Expected: {list(model_dict.keys())}"
                    f"\n  Got:      {list(item.keys())}"
                )
                return False

        except Exception as e:
            print(f"\nTRY AGAIN: Key comparison failed for item {item_index}: {e}")
            return False

        # Validate/coerce datatypes
        validated_dict = validate_datatypes_or_fail(item)
        if validated_dict is False:
            print(f"\nTRY AGAIN: Datatype validation failed for item {item_index}")
            return False

        validated_list.append(validated_dict)

    print(f"\n All {len(validated_list)} items passed validation")
    return validated_list

def run_batch_extraction_with_retry(
    raw_text_blob: str,
    target_schema_model_dict: dict,
    extraction_prompt_string: str,
    use_this_model: str,
    max_retries: int = 3,
) -> list:
    """
    Batch variant of run_extraction_with_retry().

    Intent:
    -------
    Takes a fully pre-built batch prompt string, sends it to the Mistral API,
    and attempts to extract a validated list of Python dicts matching the
    target schema. Retries up to max_retries times on failure.

    Key difference from single-row version:
    - Calls extract_json_array_from_unstructured_text() instead of
      extract_json_as_pydict_from_unstructured_text()
    - Returns a list of dicts on success, None on total failure

    Parameters:
    -----------
    raw_text_blob : str
        The concatenated batch blob. Used for session logging.
    target_schema_model_dict : dict
        The schema dict defining expected output fields.
    extraction_prompt_string : str
        The fully assembled batch prompt from build_batch_extraction_prompt().
    use_this_model : str
        The Mistral model name string.
    max_retries : int
        Maximum number of API call + extraction attempts. Default 3.

    Returns:
    --------
    list[dict]
        List of validated Python dicts matching the schema on success.
    None
        If all retries are exhausted without a valid extraction.
    """
    # --- Initialize session log ---
    session_log_filepath = initialize_session_log(
        raw_text_blob,
        target_schema_model_dict,
        use_this_model,
    )

    # --- Construct context_history ONCE ---
    context_history = []

    generic_system_instruction = (
        "You are a precise data extraction assistant. "
        "Follow the instructions exactly. "
        "Return only valid JSON in markdown format. "
        "No other text or commentary."
    )

    context_history.append(
        add_to_context_history("system", generic_system_instruction)
    )
    context_history.append(
        add_to_context_history("user", extraction_prompt_string)
    )

    print_and_log(
        (
            f"\nContext history constructed (batch mode)."
            f"\nStarting batch extraction loop."
            f"\nMax retries: {max_retries}\n"
        ),
        session_log_filepath,
    )

    # --- Retry loop ---
    for attempt_number in range(1, max_retries + 1):

        print_and_log(
            f"\n{'='*50}\nBATCH ATTEMPT {attempt_number} of {max_retries}\n{'='*50}",
            session_log_filepath,
        )

        # --- Call the API ---
        response = ask_mistral_api(context_history, use_this_model)

        # --- Extract raw reply string ---
        try:
            response_data = response.json()
            raw_reply_string = response_data["choices"][0]["message"]["content"]

        except Exception as e:
            print_and_log(
                (
                    f"\nATTEMPT {attempt_number}: Could not extract reply string"
                    f" from response object.\n{e}\n{traceback.format_exc()}"
                ),
                session_log_filepath,
            )
            continue

        print_and_log(
            f"\nRAW API RESPONSE (attempt {attempt_number}):\n{raw_reply_string}",
            session_log_filepath,
        )

        # --- Attempt JSON array extraction and validation ---
        print_and_log(
            f"\nAttempting JSON array extraction and validation (attempt {attempt_number})...",
            session_log_filepath,
        )

        try:
            validated_list = extract_json_array_from_unstructured_text(
                raw_reply_string,
                target_schema_model_dict,
            )

        except Exception as e:
            print_and_log(
                (
                    f"\nATTEMPT {attempt_number}: extract_json_array raised"
                    f" an exception.\n{e}\n{traceback.format_exc()}"
                ),
                session_log_filepath,
            )
            continue

        # --- Check result ---
        if validated_list is False:
            print_and_log(
                (
                    f"\nATTEMPT {attempt_number} FAILED:"
                    f" extraction returned False. Retrying...\n"
                ),
                session_log_filepath,
            )
            continue

        # --- SUCCESS ---
        print_and_log(
            (
                f"\nATTEMPT {attempt_number} SUCCEEDED."
                f"\nExtracted {len(validated_list)} records."
            ),
            session_log_filepath,
        )

        for idx, d in enumerate(validated_list):
            print_and_log(
                f"  Record {idx}: {json.dumps(d, indent=2, default=str)}",
                session_log_filepath,
            )

        save_extraction_results(
            raw_text_blob=raw_text_blob,
            target_schema_model_dict=target_schema_model_dict,
            use_this_model=use_this_model,
            result_dict=validated_list,
            attempts_made=attempt_number,
            session_log_filepath=session_log_filepath,
        )

        return validated_list

    # --- All attempts failed ---
    print_and_log(
        (
            f"\nWARNING: All {max_retries} attempts exhausted."
            f" Batch extraction failed. Returning None.\n"
        ),
        session_log_filepath,
    )

    save_extraction_results(
        raw_text_blob=raw_text_blob,
        target_schema_model_dict=target_schema_model_dict,
        use_this_model=use_this_model,
        result_dict=None,
        attempts_made=max_retries,
        session_log_filepath=session_log_filepath,
    )

    return None

