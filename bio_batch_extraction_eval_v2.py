def compute_row_similarity_score(
    extracted_dict: dict,
    ground_truth_dict: dict,
) -> int:
    """
    Compute a similarity score between an extracted dict and a ground-truth dict.

    Intent:
    -------
    This is a lightweight wrapper around compare_row_to_ground_truth() that
    returns just the integer count of matching fields. Used by the best-fit
    matching algorithm to rank candidate pairs.

    Higher score = better match. Maximum score = total number of fields.

    Parameters:
    -----------
    extracted_dict : dict
        A single extracted record dict.
    ground_truth_dict : dict
        A single ground-truth record dict.

    Returns:
    --------
    int
        The number of fields that matched exactly.
    """
    comparison = compare_row_to_ground_truth(
        extracted_dict=extracted_dict,
        ground_truth_dict=ground_truth_dict,
    )
    return comparison["fields_correct_count"]


def match_extracted_to_ground_truth(
    extracted_list: list,
    ground_truth_list: list,
) -> dict:
    """
    Match extracted dicts to ground-truth dicts using two-pass matching.

    Intent:
    -------
    In batch extraction, the model returns a list of dicts that may not be
    in the same order as the ground-truth rows. Some may be missing, some
    may be duplicated or hallucinated, and some may have field errors.

    This function determines which extracted dict corresponds to which
    ground-truth dict, so we can do meaningful field-level error analysis.

    Algorithm:
    ----------
    Pass 1 (Exact Match):
        For each extracted dict, check if it exactly matches any unmatched
        ground-truth dict (all fields equal). If so, pair them and remove
        both from the unmatched pools.

    Pass 2 (Best-Fit Match):
        For remaining unmatched pairs, compute similarity scores (count of
        matching fields) for every possible extracted-vs-ground-truth pair.
        Greedily assign: pick the pair with the highest score, remove both
        from pools, repeat. Only assign if similarity > 0.

    Leftovers:
        - Unmatched ground-truth dicts = missed/dropped rows
        - Unmatched extracted dicts = spurious/hallucinated rows

    Parameters:
    -----------
    extracted_list : list[dict]
        The list of dicts returned by the batch extraction pipeline.
    ground_truth_list : list[dict]
        The list of ground-truth dicts for this batch.

    Returns:
    --------
    dict
        A matching result dictionary:
        {
            "exact_matches": [
                {
                    "ground_truth_index": int,
                    "extracted_index": int,
                    "comparison": dict (from compare_row_to_ground_truth),
                },
                ...
            ],

            "best_fit_matches": [
                {
                    "ground_truth_index": int,
                    "extracted_index": int,
                    "comparison": dict,
                    "similarity_score": int,
                },
                ...
            ],

            "missed_ground_truth_indices": [int, ...],
                Ground-truth rows with no match at all.

            "spurious_extracted_indices": [int, ...],
                Extracted dicts with no reasonable ground-truth match.

            "expected_count": int,
            "extracted_count": int,
        }
    """
    # Track which indices are still available for matching
    unmatched_gt_indices = set(range(len(ground_truth_list)))
    unmatched_ex_indices = set(range(len(extracted_list)))

    exact_matches = []
    best_fit_matches = []

    # ================================================================
    # PASS 1: Exact matches
    # ================================================================
    # Iterate over a copy of unmatched extracted indices so we can
    # modify the set during iteration
    for ex_idx in list(unmatched_ex_indices):
        for gt_idx in list(unmatched_gt_indices):

            comparison = compare_row_to_ground_truth(
                extracted_dict=extracted_list[ex_idx],
                ground_truth_dict=ground_truth_list[gt_idx],
            )

            if comparison["row_match"]:
                exact_matches.append({
                    "ground_truth_index": gt_idx,
                    "extracted_index": ex_idx,
                    "comparison": comparison,
                })
                unmatched_gt_indices.discard(gt_idx)
                unmatched_ex_indices.discard(ex_idx)
                break  # This extracted dict is matched, move to next

    # ================================================================
    # PASS 2: Best-fit matching (greedy by highest similarity)
    # ================================================================
    # Build a list of all possible (ex_idx, gt_idx, score) triples
    # for remaining unmatched pairs, then greedily assign best pairs

    if unmatched_ex_indices and unmatched_gt_indices:

        candidate_pairs = []

        for ex_idx in unmatched_ex_indices:
            for gt_idx in unmatched_gt_indices:

                score = compute_row_similarity_score(
                    extracted_dict=extracted_list[ex_idx],
                    ground_truth_dict=ground_truth_list[gt_idx],
                )

                candidate_pairs.append((ex_idx, gt_idx, score))

        # Sort by score descending (best matches first)
        candidate_pairs.sort(key=lambda x: x[2], reverse=True)

        # Greedy assignment
        assigned_gt = set()
        assigned_ex = set()

        for ex_idx, gt_idx, score in candidate_pairs:

            # Skip if either index already assigned
            if ex_idx in assigned_ex or gt_idx in assigned_gt:
                continue

            # Only assign if at least one field matches
            if score <= 0:
                continue

            # Get full comparison for this pair
            comparison = compare_row_to_ground_truth(
                extracted_dict=extracted_list[ex_idx],
                ground_truth_dict=ground_truth_list[gt_idx],
            )

            best_fit_matches.append({
                "ground_truth_index": gt_idx,
                "extracted_index": ex_idx,
                "comparison": comparison,
                "similarity_score": score,
            })

            assigned_gt.add(gt_idx)
            assigned_ex.add(ex_idx)

        # Update unmatched sets
        unmatched_gt_indices -= assigned_gt
        unmatched_ex_indices -= assigned_ex

    # ================================================================
    # Leftovers
    # ================================================================
    missed_ground_truth_indices = sorted(list(unmatched_gt_indices))
    spurious_extracted_indices = sorted(list(unmatched_ex_indices))

    matching_result = {
        "exact_matches": exact_matches,
        "best_fit_matches": best_fit_matches,
        "missed_ground_truth_indices": missed_ground_truth_indices,
        "spurious_extracted_indices": spurious_extracted_indices,
        "expected_count": len(ground_truth_list),
        "extracted_count": len(extracted_list),
    }

    return matching_result

